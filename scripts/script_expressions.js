// expressões - são combinações de valores, variáveis, operadores e funções que produzem um resultado
// expressões literais - são valores fixos como numeros ou string
42;
"Olá";
true;

// expressões de variáveis - referenciam o valor armazenado em uma variável
let x = 10;
x + 5; // expressão que usa a variável x

// expressões de operadores - combinam valores e variáveis usando operadores matemáticos ou lógicos
5 + 3; // expressão de adição
x * 2; // expressão de multiplicação usando variável
true && false; // expressão lógica

// express~ões de função - chamam uma função e usam seu valor de retorno
Math.max(3, 7); // expressão que chama a função Math.max e resultado é 7